<p></p>
<p style="text-align: justify;"><strong>Установка автосигнализации.</strong> Противоугонная автомобильная система обеспечит эффективную защиту от угона и создаст дополнительный комфорт. Наличие дополнительных функций позволит вам осуществлять дистанционное управление центральным замком и электрическими стеклоподъемниками, открывать багажник автомобиля и ворота гаража, включать освещение в автомобильном салоне.</p>
<p justify=""><strong><img src="/images/lavnaya2.JPG" width="300" height="196" alt="Автосигнализации в Челябинске" title="Автосигнализации в Челябинске" style="float: right; margin: 5px 7px;" />Ремонт автостекол в Челябинске</strong>. В автосервисе "AutoStyle" осуществляется ремонт автостекол различной сложности (устранение сколов, трещин и пр.).</p>
<p justify=""><strong>Полировка авто в Челябинске</strong>. Вид выполняемой нами полировки зависит от состояния автомобильного лакокрасочного покрытия.</p>
<p justify="">Существует 2 основных разновидности полировки:</p>
<p justify=""></p>
<ul>
<li style="text-align: justify;">Легкая полировка устраняет въевшиеся частицы пыли и реагентов, придавая авто приятный глянцевый блеск. Царапины в ходе легкой полировки не удаляются.</li>
<li style="text-align: justify;">Глубокая полировка. Представляет собой восстановительную полировку кузова. Ее используют при наличии на поверхности лакокрасочного покрытия потертостей, мелких царапин, участков помутнения и прочих дефектов. Глубокая полировка позволяет устранить до девяноста восьми процентов мелких царапин и возвратить автомобилю привлекательный глянцевый блеск.</li>
</ul>
<p></p>
<p style="text-align: justify;"><strong>Бронирование автомобиля защитной пленкой</strong>. Услуга бронирования автомобиля защитной пленкой позволяет сделать использование личного автотранспорта более безопасным. От появления царапин, вмятин и прочих повреждений не застрахован ни один автомобиль. Использование защитной пленки позволяет защитить кузов от появления сколов. Защитная пленка от царапин и сколов &ndash; это эффективная защита фар и кузова автомобиля от царапин, сколов и прочих повреждений, позволяющая продлить срок их эксплуатации и сохранить лакокрасочное покрытие в первоначальном состоянии.</p>
<p justify=""><b>Преимущества антигравийной пленки:</b></p>
<ul>
<li style="text-align: justify;">визуально незаметна благодаря своей прозрачности;</li>
<li style="text-align: justify;">не меняет цвет лакокрасочного покрытия кузова;</li>
<li style="text-align: justify;">прочно держится на деталях кузова, не меняя своей первоначальной формы;</li>
<li style="text-align: justify;">обеспечивает защиту кузова от небольших повреждений во время столкновений автомобиля с препятствиями и другими транспортными средствами;</li>
</ul>
