    <h2>Услуги автосервиса</h2>
<p><img src="/images/glavnaya.png" width="300" height="183" alt="Тонировка авто в Челябинске" title="Тонировка авто в Челябинске" style="float: right; margin: 5px 7px;" /></p>

<p></p>
<p justify="">"AutoStyle" предоставляет клиентам следующие услуги:</p>
<ol>
<li style="text-align: justify;">бронирование автомобиля защитной пленкой;</li>
<li style="text-align: justify;">тонировка авто;</li>
<li style="text-align: justify;">выправление вмятин на кузове без покраски;</li>
<li style="text-align: justify;">наклейка виниловых пленок;</li>
<li style="text-align: justify;">шумоизоляция салона авто;</li>
<li style="text-align: justify;">нанесение защитных покрытий (эпоксидное покрытие, керамика, жидкое стекло);</li>
<li style="text-align: justify;">полировка фар и кузова;</li>
<li style="text-align: justify;">ремонт автостекол;</li>
<li style="text-align: justify;">установка автосигнализации в Челябинске.</li>
